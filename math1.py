from math import ceil, floor, trunc
x=1.4
y=2.6

print(floor(x),floor(y))
print(floor(-x),floor(-y))
print(ceil(x),ceil(y))
print(ceil(-x),ceil(-y))
print(trunc(x),trunc(y))
print(trunc(-x),trunc(-y))


